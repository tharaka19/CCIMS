package com.cims.client.constants.enums;

/**
 * The {@code Status} enum represents the different status values that an object or entity can have in the system.
 * These status values are used to indicate the state of an object or entity, such as whether it is active or inactive,
 * pending or completed, or suspended or deleted. The enum defines a set of named constant values that can be used to
 * represent these different statuses.
 * <p>
 * The following status values are defined:
 * <p>
 * - NONE: Represents a default or undefined status value.
 * - ACTIVE: Represents an active or enabled status value.
 * - INACTIVE: Represents an inactive or disabled status value.
 * - PENDING: Represents a pending or waiting status value.
 */
public enum Status {
    NONE,
    ACTIVE,
    INACTIVE,
    PENDING
}
