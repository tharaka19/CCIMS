package com.cims.auth.service.constants.enums;

/**
 * Enumeration of service names for a system.
 * This enum represents the different types of services available in a system,
 * which are ADMIN and USER. These values can be used to differentiate between
 * the different types of users accessing the system and the type of service
 * they are authorized to use.
 */
public enum ServiceName {
    ADMIN,
    USER
}
