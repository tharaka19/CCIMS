package com.cims.employee.constants.enums;

/**
 * The Gender enum represents the possible genders of a person.
 * <p>
 * This enum has three values: NONE, MALE, and FEMALE. The NONE value
 * can be used when a person's gender is unknown or not applicable.
 * The MALE value represents the male gender, and the FEMALE value
 * represents the female gender.
 */
public enum Gender {
    NONE,
    MALE,
    FEMALE
}
