package com.cims.employee.constants.enums;

/**
 * This enum represents the months of the year, including "NONE" as a default value.
 * It provides a convenient way of representing months as constants in the application code.
 */
public enum Month {
    NONE,
    JANUARY,
    FEBRUARY,
    MARCH,
    APRIL,
    MAY,
    JUNE,
    JULY,
    AUGUST,
    SEPTEMBER,
    OCTOBER,
    NOVEMBER,
    DECEMBER;
}
